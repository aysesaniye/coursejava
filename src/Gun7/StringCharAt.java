package Gun7;

public class StringCharAt {
    public static void main(String[] args) {
        //istenen noktadaki harfi verir, harflerin sirasi 0 dan baslar , buna index denir.
                      //0123456789012

        String kelime = "Merhana Dunya";

        char harf = kelime.charAt(3);
        System.out.println("3.harf = "+harf); // 3-> h

        harf = kelime.charAt(0);
        System.out.println("0.harf = " + harf);  //0 ->m







    }
}
